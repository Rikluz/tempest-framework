package tempest.server;
import tempest.data.Data;
import tempest.primitives.MudString;

public class Mud

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private static Data mudData;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public static Data get()            { return mudData;        }
  public static Data get(String s)    { return mudData.get(s); }
  public static Data get(MudString s) { return mudData.get(s); }
  public static void set(Data d)      { mudData = d;           }
  public static void add(Data d)      { mudData.add(d);        }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}