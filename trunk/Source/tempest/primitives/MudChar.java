package tempest.primitives;

public class MudChar

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private char value;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public MudChar()              { value = ' ';         }
  public MudChar(char c)        { value = c;           }
  public boolean equals(char c) { return (value == c); }
  public char value()           { return value;        }
  public void set(char c)       { value = c;           }
  public void set(MudChar C)    { value = C.value();   }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}